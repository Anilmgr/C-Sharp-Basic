using System;

namespace CSharpBasics
{
    class BooleanOp
    {
        static void Main( string[] args)
        {
            bool enjoyingCSharp = true;
            bool feelingLazy = false;
            Console.WriteLine(enjoyingCSharp);   // Outputs True
            Console.WriteLine(feelingLazy);   // Outputs False

            // Comparison operator returns boolean value

            Console.WriteLine(5 < 10); // Outputs True
            Console.WriteLine(5 == 10); // Outputs Falses
        }
    }
}
